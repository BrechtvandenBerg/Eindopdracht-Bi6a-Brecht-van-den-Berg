    /**
     * checks the selected item from the button group
     * fills dropdown with the tax id's 
     * sorts using the chosen sorting method
     */
    private void jComboBox2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox2ActionPerformed
        String selecteditem = jComboBox2.getSelectedItem().toString();
        selecteditem = selecteditem.substring(0,selecteditem.indexOf("(")-1);
        mylogic.setsublist2(Integer.parseInt(selecteditem));
        String selectedbutton = "";
        if (jRadioButton1.isSelected()){
            selectedbutton  = "ID";
        }else{
            selectedbutton  = "Classificatie";
        }
        mylogic.sortsublist1(selectedbutton);
        DefaultListModel model = new DefaultListModel();
        int teller = 0;
        for (Virus myvirus : mylogic.hostsublist2){
            model.add(teller, myvirus);
            teller++;
        }
        
        jList2.setCellRenderer(new VirusListCellRenderer());
        jList2.setModel(model);
        setoverlap();
    }//GEN-LAST:event_jComboBox2ActionPerformed

    /**
     * prepares the tax id's to be added into the dropdown menu's
     */
    public class VirusListCellRenderer extends DefaultListCellRenderer {
    public Component getListCellRendererComponent(JList<?> list,
            Object value,
            int index,
            boolean isSelected,
            boolean cellHasFocus) {
        super.getListCellRendererComponent(list, value, index, isSelected, cellHasFocus);
        if (value instanceof Virus) {
            Virus myvirus = (Virus)value;
            setText(myvirus.getVirus_tax_id()+"");
            setToolTipText(myvirus.getVirus_name()+"");
        }
        return this;
    }
}

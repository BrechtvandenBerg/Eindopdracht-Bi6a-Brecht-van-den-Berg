    /**
     * fills the third textfield with the overlap
     */
    private void setoverlap(){
        mylogic.overlap();
        DefaultListModel model = new DefaultListModel();
        int teller = 0;
        for (Virus myvirus : mylogic.overlaplist){
            model.add(teller, myvirus);
            
            teller++;
        }
        
        jList3.setCellRenderer(new VirusListCellRenderer());
        jList3.setModel(model);
    }
    
    /**
     * adds the tax id's to the dropdown menu's
     */
    private void hostidboxinit(){
        
        for (String item : mylogic.hostidset){
            jComboBox2.addItem(item);
            jComboBox3.addItem(item);
            
        }
    }
